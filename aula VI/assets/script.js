//Tipos de função
//declarativa
function funcao(){
    console.log('Sou uma função declarativa');
}

funcao();

//Expresões de funções
//com nomeação
var funcaoo = function funcao(){
    console.log('Função com nomeação!');
}

funcaoo();

//Arrow function
var funcaao = () => {
    console.log('Sou uma arrow function');
}

funcaao();